-- Materials / chemicals / equipment catalog for the internal Materialien module
-- Run this in Supabase SQL Editor

CREATE TABLE IF NOT EXISTS public.report_checklist_items (
  id uuid PRIMARY KEY DEFAULT gen_random_uuid(),
  category text NOT NULL CHECK (category IN ('chemie', 'geraete', 'materialien', 'hinweise')),
  name text NOT NULL,
  description text,
  warning text,
  safety_sheet_url text,
  active boolean DEFAULT true,
  created_at timestamptz DEFAULT now()
);

ALTER TABLE public.report_checklist_items
  ADD COLUMN IF NOT EXISTS category text,
  ADD COLUMN IF NOT EXISTS name text,
  ADD COLUMN IF NOT EXISTS description text,
  ADD COLUMN IF NOT EXISTS warning text,
  ADD COLUMN IF NOT EXISTS safety_sheet_url text,
  ADD COLUMN IF NOT EXISTS active boolean DEFAULT true,
  ADD COLUMN IF NOT EXISTS created_at timestamptz DEFAULT now();

CREATE INDEX IF NOT EXISTS idx_report_checklist_items_category
  ON public.report_checklist_items(category, active, name);

ALTER TABLE public.report_checklist_items ENABLE ROW LEVEL SECURITY;

DROP POLICY IF EXISTS "Checklist read active" ON public.report_checklist_items;
DROP POLICY IF EXISTS "Checklist admin insert" ON public.report_checklist_items;
DROP POLICY IF EXISTS "Checklist admin update" ON public.report_checklist_items;
DROP POLICY IF EXISTS "Checklist admin delete" ON public.report_checklist_items;

CREATE POLICY "Checklist read active"
  ON public.report_checklist_items
  FOR SELECT
  TO authenticated
  USING (active = true OR lower(auth.jwt() ->> 'email') = 'mtclemur@gmail.com');

CREATE POLICY "Checklist admin insert"
  ON public.report_checklist_items
  FOR INSERT
  TO authenticated
  WITH CHECK (lower(auth.jwt() ->> 'email') = 'mtclemur@gmail.com');

CREATE POLICY "Checklist admin update"
  ON public.report_checklist_items
  FOR UPDATE
  TO authenticated
  USING (lower(auth.jwt() ->> 'email') = 'mtclemur@gmail.com')
  WITH CHECK (lower(auth.jwt() ->> 'email') = 'mtclemur@gmail.com');

CREATE POLICY "Checklist admin delete"
  ON public.report_checklist_items
  FOR DELETE
  TO authenticated
  USING (lower(auth.jwt() ->> 'email') = 'mtclemur@gmail.com');

