CREATE POLICY "Material request items select"
  ON public.material_request_items FOR SELECT TO authenticated
  USING (
    EXISTS (
      SELECT 1 FROM public.material_requests request
      WHERE request.id = material_request_id
        AND (
          request.user_id = auth.uid()
          OR lower(auth.jwt() ->> 'email') = 'mtclemur@gmail.com'
          OR EXISTS (
            SELECT 1 FROM public.workers worker
            WHERE lower(worker.email) = lower(auth.jwt() ->> 'email')
              AND lower(worker.role) = 'vorarbeiter'
          )
        )
    )
  );

CREATE POLICY "Material request items insert"
  ON public.material_request_items FOR INSERT TO authenticated
  WITH CHECK (
    EXISTS (
      SELECT 1 FROM public.material_requests request
      WHERE request.id = material_request_id
