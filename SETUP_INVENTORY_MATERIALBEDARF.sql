CREATE POLICY "Material items read"
  ON public.material_items FOR SELECT TO authenticated
  USING (active = true OR lower(auth.jwt() ->> 'email') = 'mtclemur@gmail.com');

CREATE POLICY "Material items admin write"
  ON public.material_items FOR ALL TO authenticated
  USING (lower(auth.jwt() ->> 'email') = 'mtclemur@gmail.com')
  WITH CHECK (lower(auth.jwt() ->> 'email') = 'mtclemur@gmail.com');

CREATE POLICY "Material requests select"
  ON public.material_requests FOR SELECT TO authenticated
  USING (
    auth.uid() = user_id
    OR lower(auth.jwt() ->> 'email') = 'mtclemur@gmail.com'
    OR EXISTS (
      SELECT 1 FROM public.workers worker
      WHERE lower(worker.email) = lower(auth.jwt() ->> 'email')
        AND lower(worker.role) = 'vorarbeiter'
    )
  );

CREATE POLICY "Material requests insert own"
  ON public.material_requests FOR INSERT TO authenticated
  WITH CHECK (auth.uid() IS NOT NULL AND user_id = auth.uid());

