CREATE TABLE IF NOT EXISTS public.material_items (
  id uuid PRIMARY KEY DEFAULT gen_random_uuid(),
  name text NOT NULL,
  category text DEFAULT 'verbrauchsmaterial',
  unit text,
  image_url text,
  active boolean DEFAULT true,
  created_at timestamptz DEFAULT now(),
  updated_at timestamptz DEFAULT now()
);

ALTER TABLE public.material_items
  ADD COLUMN IF NOT EXISTS name text,
  ADD COLUMN IF NOT EXISTS category text DEFAULT 'verbrauchsmaterial',
  ADD COLUMN IF NOT EXISTS unit text,
  ADD COLUMN IF NOT EXISTS image_url text,
  ADD COLUMN IF NOT EXISTS active boolean DEFAULT true,
  ADD COLUMN IF NOT EXISTS created_at timestamptz DEFAULT now(),
  ADD COLUMN IF NOT EXISTS updated_at timestamptz DEFAULT now();

CREATE INDEX IF NOT EXISTS idx_material_items_active_name
  ON public.material_items(active, name);

CREATE TABLE IF NOT EXISTS public.material_requests (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  objekt_id uuid,
  objekt_name text,
  auftrag_id text,
  user_id uuid REFERENCES auth.users(id) ON DELETE SET NULL DEFAULT auth.uid(),
  reporter_name text,
  reporter_email text,
  request_date date NOT NULL DEFAULT CURRENT_DATE,
  status text NOT NULL DEFAULT 'offen' CHECK (status IN ('offen', 'geplant', 'eingepackt', 'mitgenommen', 'erledigt')),
  note text,
  summary_sent boolean DEFAULT false,
  summary_sent_at timestamptz,
  created_at timestamptz DEFAULT now(),
  updated_at timestamptz DEFAULT now()
);

ALTER TABLE public.material_requests
  ADD COLUMN IF NOT EXISTS objekt_id uuid,
  ADD COLUMN IF NOT EXISTS objekt_name text,
  ADD COLUMN IF NOT EXISTS auftrag_id text,
  ADD COLUMN IF NOT EXISTS user_id uuid REFERENCES auth.users(id) ON DELETE SET NULL DEFAULT auth.uid(),
  ADD COLUMN IF NOT EXISTS reporter_name text,
  ADD COLUMN IF NOT EXISTS reporter_email text,
  ADD COLUMN IF NOT EXISTS request_date date DEFAULT CURRENT_DATE,
  ADD COLUMN IF NOT EXISTS status text DEFAULT 'offen',
  ADD COLUMN IF NOT EXISTS note text,
  ADD COLUMN IF NOT EXISTS summary_sent boolean DEFAULT false,
  ADD COLUMN IF NOT EXISTS summary_sent_at timestamptz,
  ADD COLUMN IF NOT EXISTS created_at timestamptz DEFAULT now(),
  ADD COLUMN IF NOT EXISTS updated_at timestamptz DEFAULT now();

CREATE INDEX IF NOT EXISTS idx_material_requests_date_status
  ON public.material_requests(request_date, status, summary_sent);
CREATE INDEX IF NOT EXISTS idx_material_requests_user
  ON public.material_requests(user_id, request_date);

CREATE TABLE IF NOT EXISTS public.material_request_items (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  material_request_id bigint REFERENCES public.material_requests(id) ON DELETE CASCADE,
  material_item_id uuid REFERENCES public.material_items(id) ON DELETE SET NULL,
  custom_name text,
  quantity numeric,
  unit text,
  note text,
  created_at timestamptz DEFAULT now()
);

ALTER TABLE public.material_request_items
  ADD COLUMN IF NOT EXISTS material_request_id bigint REFERENCES public.material_requests(id) ON DELETE CASCADE,
  ADD COLUMN IF NOT EXISTS material_item_id uuid REFERENCES public.material_items(id) ON DELETE SET NULL,
  ADD COLUMN IF NOT EXISTS custom_name text,
  ADD COLUMN IF NOT EXISTS quantity numeric,
  ADD COLUMN IF NOT EXISTS unit text,
  ADD COLUMN IF NOT EXISTS note text,
  ADD COLUMN IF NOT EXISTS created_at timestamptz DEFAULT now();

CREATE INDEX IF NOT EXISTS idx_material_request_items_request
  ON public.material_request_items(material_request_id);

CREATE TABLE IF NOT EXISTS public.inventory_items (
  id uuid PRIMARY KEY DEFAULT gen_random_uuid(),
  name text NOT NULL,
  category text NOT NULL,
  image_url text,
  status text NOT NULL DEFAULT 'verfuegbar' CHECK (status IN ('verfuegbar', 'unterwegs', 'pruefen', 'beschaedigt')),
  active boolean DEFAULT true,
  created_at timestamptz DEFAULT now(),
  updated_at timestamptz DEFAULT now()
);

ALTER TABLE public.inventory_items
  ADD COLUMN IF NOT EXISTS name text,
  ADD COLUMN IF NOT EXISTS category text,
  ADD COLUMN IF NOT EXISTS image_url text,
  ADD COLUMN IF NOT EXISTS status text DEFAULT 'verfuegbar',
  ADD COLUMN IF NOT EXISTS active boolean DEFAULT true,
  ADD COLUMN IF NOT EXISTS created_at timestamptz DEFAULT now(),
  ADD COLUMN IF NOT EXISTS updated_at timestamptz DEFAULT now();

CREATE INDEX IF NOT EXISTS idx_inventory_items_status
  ON public.inventory_items(status, active, category, name);

CREATE TABLE IF NOT EXISTS public.inventory_checkouts (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  inventory_item_id uuid REFERENCES public.inventory_items(id) ON DELETE CASCADE,
  user_id uuid REFERENCES auth.users(id) ON DELETE SET NULL DEFAULT auth.uid(),
  worker_name text,
  worker_email text,
  object_id uuid,
  object_name text,
  report_id bigint,
  taken_at timestamptz DEFAULT now(),
  returned_at timestamptz,
  return_clean boolean DEFAULT false,
  return_functional boolean DEFAULT false,
  return_note text,
  status text DEFAULT 'unterwegs',
  created_at timestamptz DEFAULT now(),
  updated_at timestamptz DEFAULT now()
);

ALTER TABLE public.inventory_checkouts
  ADD COLUMN IF NOT EXISTS inventory_item_id uuid REFERENCES public.inventory_items(id) ON DELETE CASCADE,
  ADD COLUMN IF NOT EXISTS user_id uuid REFERENCES auth.users(id) ON DELETE SET NULL DEFAULT auth.uid(),
  ADD COLUMN IF NOT EXISTS worker_name text,
  ADD COLUMN IF NOT EXISTS worker_email text,
  ADD COLUMN IF NOT EXISTS object_id uuid,
  ADD COLUMN IF NOT EXISTS object_name text,
  ADD COLUMN IF NOT EXISTS report_id bigint,
  ADD COLUMN IF NOT EXISTS taken_at timestamptz DEFAULT now(),
  ADD COLUMN IF NOT EXISTS returned_at timestamptz,
  ADD COLUMN IF NOT EXISTS return_clean boolean DEFAULT false,
  ADD COLUMN IF NOT EXISTS return_functional boolean DEFAULT false,
  ADD COLUMN IF NOT EXISTS return_note text,
  ADD COLUMN IF NOT EXISTS status text DEFAULT 'unterwegs',
  ADD COLUMN IF NOT EXISTS created_at timestamptz DEFAULT now(),
  ADD COLUMN IF NOT EXISTS updated_at timestamptz DEFAULT now();

CREATE INDEX IF NOT EXISTS idx_inventory_checkouts_open
  ON public.inventory_checkouts(returned_at, user_id, taken_at);

